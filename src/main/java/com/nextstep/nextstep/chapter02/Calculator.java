package com.nextstep.nextstep.chapter02;

public class Calculator {

    int sum(int i, int j){
        return i + j;
    }

    int num(int i, int j){
        return i - j;
    }

    int multiply(int i, int j){
        return i * j;
    }

    int divide(int i, int j){
        return i / j;
    }

}
